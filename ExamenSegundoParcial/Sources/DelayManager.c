void DelayManager_Init(void)
{
	//Set up default MTIM configuration
	MTIM_Init();
	(void)MTIM_SelectClkSource(BUS_CLK); //BUSCLK
	(void)MTIM_SelectPrescaler(DIVIDED_BY_32); //PRESCALER 1/32
	
	//Configure MTIM for 1785.750 us
	MTIM_SetTimerModulo(MODULO_VAL);
}

void DelayManager_SetDelayBySpeed(char u8speed)
{
	char u8extraFlags = 0;
	//Calculate the extra flags requires for this speed
	u8extraFlags = 255 - u8speed;
	MTIM_SetExtraFlags(u8extraFlags);
}

void DelayManager_Delay(void)
{
	MTIM_Start();
}

char DelayManager_IsBusy(void)
{
	return MTIM_IsRunning();
}

