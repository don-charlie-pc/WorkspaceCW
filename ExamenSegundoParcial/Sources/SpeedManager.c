void SpeedManager_Init(void)
{
	//Initialise ADC controller
	ADC_Init();	
	ADC_selectChannel(1);
}

char SpeedManager_ReadSpeedLevel(void)
{
	char u8AdcValue = 0;
	char u8speed = 0;
	
	ADC_StartConversion();
	//Wait until ADC conversion ends
	while(!ADC_Read_8(&u8AdcValue)); 
	return u8speed;
}

