void main(void) {
	
	StepperMotor_Init();
	SpeedManager_Init();
	DelayManager_Init();
	EnableInterrupts;
	
  
	for(;;)
	{
		int var = 0x01;
	  var = var << 2;
	  __RESET_WATCHDOG();
	  DelayManager_SetDelay(SpeedManager_ReadSpeed());
	  DelayManager_Delay();
	  StepperMotor_NextStep();
	  StepperMotor_RefreshDirection(); 
	}
}
