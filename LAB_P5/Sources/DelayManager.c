void DelayManager_Init(void)
{
	//Set up default TMP configuration
	TPM_Init();
	TPM_SetMicroSecPFlag(1930);
}

char DelayManager_SetDelayBy10Rpm(unsigned int u16rpmSpeed)
{
	
	if(u16rpmSpeed >= MIN_LEVEL_SPEED && u16rpmSpeed <= MAX_LEVEL_SPEED)
	{
		u16delaytime = (unsigned int) (1920 + (75*(3500-u16rpmSpeed)/10));
		TPM_SetMicroSecPFlag((1920 + (75*(350-(u16rpmSpeed/10)))));
		return 1;
	}
	return 0;
}

void DelayManager_Delay(void)
{
	TPM_Start();
}

char DelayManager_IsBusy(void)
{
	return TMP_IsRunning();
}

